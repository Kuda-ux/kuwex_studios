export function generateMetadata({ params }: { params: { slug: string } }): Metadata {
  const meta = postMeta[params.slug];
  const baseUrl = "https://kuwexstudios.co.zw";
  const postUrl = `${baseUrl}/blog/${params.slug}`;

  if (!meta) {
    return {
      title: "Blog | KuWeX Studios",
      openGraph: {
        title: "KuWeX Studios Blog",
        description: "Latest insights on digital marketing, web design, SEO, and technology in Zimbabwe.",
        url: `${baseUrl}/blog`,
        siteName: "KuWeX Studios",
        images: [{ url: `${baseUrl}/logo.jpg`, width: 800, height: 600, alt: "KuWeX Studios" }],
        type: "website",
      },
    };
  }

  return {
    title: meta.title,
    description: meta.description,
    keywords: meta.keywords,
    authors: [{ name: meta.author, url: baseUrl }],
    openGraph: {
      title: meta.title,
      description: meta.description,
      url: postUrl,
      siteName: "KuWeX Studios",
      type: "article",
      publishedTime: meta.date,
      authors: [meta.author],
      images: [
        {
          url: meta.image,
          width: 1200,
          height: 630,
          alt: meta.title,
        },
      ],
    },
    twitter: {
      card: "summary_large_image",
      title: meta.title,
      description: meta.description,
      images: [meta.image],
      creator: "@kuwexstudios",
      site: "@kuwexstudios",
    },
    alternates: {
      canonical: postUrl,
    },
  };
}

